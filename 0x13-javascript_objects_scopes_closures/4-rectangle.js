#!/usr/bin/node
// Class that defines a Rectangle
// take two args and validates there are no equal to 0
// Instance Method called print, to print the Rect with X
// Instance Method rotate, exchange values of width and height
// Instance Method double multiplies w and h by 2
class Rectangle {
  constructor (w, h) {
    if (w > 0 && h > 0) {
      this.width = w;
      this.height = h;
    }
  }

  print () {
    let i;
    for (i = 0; i < this.height; i++) {
      console.log('X'.repeat(this.width));
    }
  }

  rotate () {
    const temp = this.height;
    this.height = this.width;
    this.width = temp;
  }

  double () {
    this.width = this.width * 2;
    this.height = this.height * 2;
  }
}

module.exports = Rectangle;
